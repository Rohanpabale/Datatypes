package basicDataType;

public class Laptop {
	public static void main(String[] args) {
		int ram = 16; // in gb
		char grade = 'A';
		long modelNumber = 1234567890L;
		float weight = 1.5F;// in kg
		double screenSize = 12.4;
		String brandName = "HP";
		int price = 45000;
		short storage = 256; //in gb

		System.out.println("Ram is " + ram);
		System.out.println("Grade is " + grade);
		System.out.println("Model number is " + modelNumber);
		System.out.println("Weight is " + weight);
		System.out.println("Screen Size is " + screenSize);
		System.out.println("brand name is " + brandName);
		System.out.println("price of laptop " + price);
		System.out.println("Storage is " + storage);

	}
}
